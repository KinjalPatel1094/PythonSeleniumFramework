# datatypes

# numeric , string , list , tuple , dictionary

# list

value = [1, 2, 3, 4, 5, "kinjal"]

print(value[0])

value.insert(3, "hey")

print(value[1:4])

print(value[-1])

value.append('patel')

value[4] = 'kem cho'

del value[0]

print(value)

# tuple datatype

car_names = ('bmw', 'merce', 'tesla', 1, 2, 3)

print(type(car_names))

# dictionary

disc = {1:"hi", 2:"hello", "third": "ok" , 4 : 4, "fifth": 5}

print(disc)

print(disc[4])

print(disc["fifth"])

# empty dictionary

disc_new = {}

disc_new['firstname'] = 'kinjal'

disc_new['lastname'] = 'patel'

disc_new[3] = 3

disc_new[4] = "shivbaba"

print(disc_new)
